export const seedBusinesses = async (userId?: string) => {
  const collectionRef = collection(db, 'businesses');
  const results = [];
  
  for (const business of businesses) {
    try {
      const docRef = await addDoc(collectionRef, {
        ...business,
        createdAt: serverTimestamp(),
        updatedAt: serverTimestamp(),
        userId: userId || 'system_seed',
        boostExpiresAt: business.isFeatured ? new Date(Date.now() + 30 * 24 * 60 * 60 * 1000) : null,
      });
      results.push({ id: docRef.id, name: business.name, status: 'success' });
    } catch (error) {
      console.error(`Error adding ${business.name}:`, error);
      results.push({ name: business.name, status: 'error', error });
    }
  }
  
  return results;
};
